/*
Abaixo será mostrado dois exemplo de arrow function, uma delas a comum e a outra uma arrow da arrow kkk, ou seja, a abreviação da abreviação.
*/

let multiplicar = (x) => {
  return x * 2;
};
console.log(multiplicar(3));

let multiplicar2 = (x) => x * 2;
console.log(multiplicar2(6)); //Nesta arrow a função foi passada direta, sem a necessidade do return, pois ele vem em seguida do parâmetro.
