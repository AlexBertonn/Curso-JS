window.addEventListener("keydown", function (e) {
  if (e.key == "Enter") {
    alert("Para de apertar Enter cabaço!!");
  } else if (e.key == " ") {
    alert("Ô CARAIO!");
  }
});

/*
Este evento é basicamente um evento atrelado há uma tecla, que ao ser pressionada efetuará algum tipo de ação no browser.
window.addEventListener
*/
